/**
 * 工具函数 - 档案类型与审核部门映射
 * 
 * 审核部门规则：
 *   科研成果        → 科研管理中心
 *   教学成果/教学改革 → 教务管理中心
 *   培训记录/进修培训 → 教师教学发展中心
 *   职称晋升/个人学历/基础信息 → 人力资源中心
 */

const DEPT_MAP = {
  '科研成果': '科研管理中心',
  '教学成果': '教务管理中心',
  '教学改革': '教务管理中心',
  '获奖情况': '教务管理中心',
  '培训记录': '教师教学发展中心',
  '进修培训': '教师教学发展中心',
  '职称晋升': '人力资源中心',
  '个人学历': '人力资源中心',
  '基础信息': '人力资源中心',
}

/**
 * 根据档案类型返回对应审核部门
 * @param {string} type - 档案类型
 * @returns {string} 审核部门名称
 */
function getReviewDept(type) {
  return DEPT_MAP[type] || '综合管理部门'
}

/**
 * 所有档案类型列表（含新增类型）
 */
const ARCHIVE_TYPES = [
  '科研成果', '教学成果', '教学改革', '获奖情况',
  '培训记录', '进修培训', '职称晋升', '个人学历', '基础信息'
]

module.exports = { getReviewDept, ARCHIVE_TYPES, DEPT_MAP }
